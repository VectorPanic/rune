/**
 * ...
 *
 * @param {number} step ...
 *
 * @return {undefined}
 * @private
 */
demo.scene.Scene002.prototype.m_updateCamera = function(step) {
    if (this.keyboard.pressed("w")) {
        this.cameras.getCamera(0).viewport.y -= 1;
    } else if (this.keyboard.pressed("s")) {
        this.cameras.getCamera(0).viewport.y += 1;
    }
    
    if (this.keyboard.pressed("a")) {
        this.cameras.getCamera(0).viewport.x -= 1;
    } else if (this.keyboard.pressed("d")) {
        if (this.keyboard.pressed("shift")) {
            this.cameras.getCamera(0).viewport.x += 15;
        } else {
            this.cameras.getCamera(0).viewport.x += 1;
        }
    }
    
    if (this.keyboard.pressed("Z")) {
        this.cameras.getCamera(0).viewport.zoom += 0.01;
    } else if (this.keyboard.pressed("X")) {
        this.cameras.getCamera(0).viewport.zoom -= 0.01;
    }
    
    if (this.keyboard.justPressed("space")) {
        this.stage.tilemap.clear();
    }
};